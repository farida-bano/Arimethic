// Arithmetic Operators
let a: number = 10;
let b: number = 5;

// Addition
let sum: number = a + b;
console.log("Sum:", sum); // Output: 15

// Subtraction
let difference: number = a - b;
console.log("Difference:", difference); // Output: 5

// Multiplication
let product: number = a * b;
console.log("Product:", product); // Output: 50

// Division
let quotient: number = a / b;
console.log("Quotient:", quotient); // Output: 2

// Modulus (Remainder)
let remainder: number = a % b;
console.log("Remainder:", remainder); // Output: 0

// Unary Operators
let x: number = 5;

// Unary Plus (+)
let unaryPlus: number = +x;
console.log("Unary Plus:", unaryPlus); // Output: 5

// Unary Negation (-)
let unaryNegation: number = -x;
console.log("Unary Negation:", unaryNegation); // Output: -5

// Increment (++)
let increment: number = x++;
console.log("Increment:", increment); // Output: 5 (x is now 6)

// Decrement (--)
let decrement: number = x--;
console.log("Decrement:", decrement); // Output: 6 (x is now 5)
